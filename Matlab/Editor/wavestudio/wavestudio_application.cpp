void WaveStudioApplication::drawTracks() {
    for (auto &track : app_state.project.tracks) {
        ImGui::BeginGroup();
        ImGui::Text("%s", track.name.c_str());
        ImGui::Text("-");
        ImGui::SameLine();
        ImGui::SetNextItemWidth(70);
        ImGui::SliderFloat("", &track.gain, 0.f, 1.f, "%.3f", 0);
        ImGui::SameLine();
        ImGui::Text("+");
        ImGui::Text("L");
        ImGui::SameLine();
        ImGui::SetNextItemWidth(70);
        ImGui::SliderFloat("", &track.pan, -1.f, 1.f, "%.3f", 0);
        ImGui::SameLine();
        ImGui::Text("R");
        ImGui::Button("Mute");
        ImGui::SameLine();
        ImGui::Button("Solo");
        ImGui::EndGroup();
        ImGui::SameLine();
        showTrackContent(track);
    }
}

void WaveStudioApplication::showOpenFileDialog(std::string &path, const char *filters, const char *dir) {
    if (!app_state.open_file_dialog_visible) {
        ImGui::OpenPopup("Open File");
        app_state.open_file_dialog_visible = true;
    }
    imgui_addons::ImGuiFileBrowser &file_dialog = app_state.file_dialog;
    if (file_dialog.showFileDialog("Open File", imgui_addons::ImGuiFileBrowser::DialogMode::OPEN, ImVec2(700, 310),
                                   filters)) {
        std::cout << file_dialog.selected_fn
                  << std::endl;      // The name of the selected file or directory in case of Select Directory dialog mode
        std::cout << file_dialog.selected_path << std::endl;    // The absolute path to the selected file
        app_state.open_file_dialog_visible = false;
        app_state.opening_sound_file = false;
    }
}

void WaveStudioApplication::showMainWindow() {
    ImGui::Begin("Project", nullptr,
                 ImGuiWindowFlags_NoResize |
                 ImGuiWindowFlags_NoCollapse |
                 ImGuiWindowFlags_MenuBar);
    //        ShowAppMainMenuBar();
    if (ImGui::BeginMenuBar()) {
        if (ImGui::BeginMenu("File")) {
            if (ImGui::MenuItem("New Project")) {}
            if (ImGui::MenuItem("Open Project")) {}
            ImGui::Separator();
            if (ImGui::MenuItem("Open Sound File")) {
                app_state.open_file_dialog_visible = false;
                app_state.opening_sound_file = true;
            }
            ImGui::MenuItem("Import Sound File");
            ImGui::Separator();
            if (ImGui::MenuItem("Quit")) {
                app_state.quit = true;
            }
            ImGui::EndMenu();
        }
    }
    ImGui::EndMenuBar();
    if (app_state.opening_sound_file) {
        std::string soundFilePath;
        showOpenFileDialog(soundFilePath, ".wav", ".");
    }
    drawTracks();
    ImGui::End();
}

void WaveStudioApplication::newProject() {
    app_state.project = Project{};
    Project &project = app_state.project;
    project.name = "Untitled";
    project.tracks = {Track{"track1", 0.5}};
}

void WaveStudioApplication::showStartupWindow() {
    ImGui::Begin("Intro Screen", &app_state.intro_window_visible,
                 ImGuiWindowFlags_NoResize);
    if (ImGui::Button("New Project")) {
        std::cout << __PRETTY_FUNCTION__ << ':' << __LINE__ << '\n';
        newProject();
        ImGuiViewport *viewport = ImGui::GetMainViewport();
        //default is 30s scale
        app_state.initial_track_width_scale = viewport->WorkSize.x / 30;
        app_state.intro_window_visible = false;
    }
    ImGui::Button("Open Project");
    ImGui::Button("Open File");
    ImGui::End();
}
